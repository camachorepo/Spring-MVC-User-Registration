package springwork.model;

import java.util.Date;

public class PrivateInfo {

	private long ss; 
	private long cc;
	private Date dob;
	
	public long getSs() {
		return ss;
	}
	public void setSs(long ss) {
		this.ss = ss;
	}
	public long getCc() {
		return cc;
	}
	public void setCc(long cc) {
		this.cc = cc;
	}
	public Date getDob() {
		return dob;
	}
	public void setDob(Date dob) {
		this.dob = dob;
	}
	
	
}
